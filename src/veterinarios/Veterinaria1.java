    public void Guardar() {
        try {

            Connection connection = DriverManager.getConnection(con.getUrl(),
                    con.getUsername(), con.getPassword());
            PreparedStatement stmt = connection.prepareStatement("call InsertarConsulta (?,?,?, ?, ?,?, ?, ?)");

            stmt.setInt(1, comboVeterinario.getSelectedIndex());
            stmt.setString(2, txtDocumento.getText());
            stmt.setString(3, txtNombreCliente.getText());
            stmt.setString(4, txtNombrePaciente.getText());
            stmt.setInt(5, comboPacientes.getSelectedIndex());
            stmt.setInt(6, comboConsulta.getSelectedIndex());
            stmt.setDouble(7, valorTotal);
            stmt.setString(8, atxtObservaciones.getText());

            stmt.executeUpdate();

            stmt.close();
            connection.close();
            JOptionPane.showMessageDialog(rootPane, "Guardado con exito");
        } catch (SQLException ex) {

            System.out.println(ex);
            JOptionPane.showMessageDialog(rootPane, "Error");
        }

        txtNombreCliente.setText("");
        txtNombrePaciente.setText("");
        txtDocumento.setText("");

        atxtObservaciones.setText("");
        comboConsulta.setSelectedIndex(0);
        comboPacientes.setSelectedIndex(0);
        comboVeterinario.setSelectedIndex(0);

    }

