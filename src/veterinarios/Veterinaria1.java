    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!txtNombreCliente.getText().isEmpty()
                && !txtDocumento.getText().isEmpty()
                && !txtNombrePaciente.getText().isEmpty()
                && comboConsulta.getSelectedIndex() > 0
                && comboPacientes.getSelectedIndex() > 0
                && comboVeterinario.getSelectedIndex() > 0) {
            int indicePaciente = comboPacientes.getSelectedIndex() - 1;
            int indiceConsulta = comboConsulta.getSelectedIndex() - 1;

            double valorpaciente = listadoPacientes.get(indicePaciente).getValor();
            double valorconsulta = listadoConsultas.get(indiceConsulta).getPrecioConsulta();
            valorTotal = valorconsulta + (valorconsulta * valorpaciente);
            int respuesta = JOptionPane.showConfirmDialog(
                    null,
                    "¿Deseas guardar la consulta con un valor de: $" + valorTotal + "?",
                    "Confirmar Guardado",
                    JOptionPane.YES_NO_OPTION);

            switch (respuesta) {
                case JOptionPane.YES_OPTION:
                    Guardar();
                    break;
                case JOptionPane.NO_OPTION:
                    JOptionPane.showMessageDialog(null, "El guardado ha sido cancelado.");
                    break;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error Campos Vacios");
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

